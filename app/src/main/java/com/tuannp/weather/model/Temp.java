public class Temp
{
    private double day;

    public double getDay() { return this.day; }

    public void setDay(double day) { this.day = day; }

    private double min;

    public double getMin() { return this.min; }

    public void setMin(double min) { this.min = min; }

    private double max;

    public double getMax() { return this.max; }

    public void setMax(double max) { this.max = max; }

    private double night;

    public double getNight() { return this.night; }

    public void setNight(double night) { this.night = night; }

    private double eve;

    public double getEve() { return this.eve; }

    public void setEve(double eve) { this.eve = eve; }

    private double morn;

    public double getMorn() { return this.morn; }

    public void setMorn(double morn) { this.morn = morn; }
}
